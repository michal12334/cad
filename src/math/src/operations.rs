pub fn multiply_quaternions(
    q1: (f64, f64, f64, f64),
    q2: (f64, f64, f64, f64),
) -> (f64, f64, f64, f64) {
    (
        q1.3 * q2.0 + q1.0 * q2.3 + q1.1 * q2.2 - q1.2 * q2.1,
        q1.3 * q2.1 - q1.0 * q2.2 + q1.1 * q2.3 + q1.2 * q2.0,
        q1.3 * q2.2 + q1.0 * q2.1 - q1.1 * q2.0 + q1.2 * q2.3,
        q1.3 * q2.3 - q1.0 * q2.0 - q1.1 * q2.1 - q1.2 * q2.2,
    )
}
